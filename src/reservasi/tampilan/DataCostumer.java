    protected void datatable(){
        Object[] Baris ={"No Costumer", "Nama Costumer", "Telpon Costumer", "Alamat Costumer"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_costumer.setModel(tabmode);
        String sql = "select * from costumer";
        try{
            java.sql.Statement stat = conn.createStatement ();
            ResultSet hasil = stat.executeQuery (sql);
            while(hasil.next()){
                String no_costumer= hasil.getString("No_Costumer");
                String nama_costumer = hasil.getString("Nama_Costumer");
                String telpon_costumer = hasil.getString("Telpon_Costumer");
                String alamat_costumer= hasil.getString("Alamat_Costumer");
                String[] data={no_costumer,nama_costumer,telpon_costumer,alamat_costumer};
                tabmode.addRow(data);
            }
        }catch (Exception e){
            
        }
            
        }
  

