    private void tbl_costumerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_costumerMouseClicked
        // TODO add your handling code here:
        try{
            int row=tbl_costumer.getSelectedRow();
            String tabel_klik=(tbl_costumer.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("Select * from costumer where No_Costumer='"+tabel_klik+"'");
            if(sql.next()){
                String no_costumer = sql.getString("no_costumer");
                    tf_nocostumer.setText(no_costumer);
                String nama_costumer = sql.getString("nama_costumer");
                    tf_namacostumer.setText(nama_costumer);
                String telpon_costumer = sql.getString("telpon_costumer");
                    tf_telponcostumer.setText(telpon_costumer);
                String alamat_costumer = sql.getString("alamat_costumer");
                    tf_alamatcostumer.setText(alamat_costumer);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Tidak ada data yang anda pilih.");
        }
        
    }//GEN-LAST:event_tbl_costumerMouseClicked

