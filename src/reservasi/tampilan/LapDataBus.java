    protected void datatable(){
        Object[] Baris ={"No Pol", "Jenis", "Merk", "Seat", "Kondisi", "Id Driver", "Nama Driver", "Telpon Driver"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_bus.setModel(tabmode);
        String sql = "select * from bus";
        try{
            java.sql.Statement stat = conn.createStatement ();
            ResultSet hasil = stat.executeQuery (sql);
            while(hasil.next()){
                String no_pol = hasil.getString("No_pol");
                String jenis = hasil.getString("Jenis");
                String merk = hasil.getString("Merk");
                String seat = hasil.getString("Seat");
                String kondisi = hasil.getString("Kondisi");
                String id_driver = hasil.getString("Id_Driver");
                String nama_driver = hasil.getString("Nama_Driver");
                String telpon_driver = hasil.getString("Telpon_Driver");
                String[] data={no_pol,jenis,merk,seat,kondisi,id_driver,nama_driver,telpon_driver};
                tabmode.addRow(data);
            }
        }catch (Exception e){
            
        }
            
        }

